
public class palestra {
	String titulo;
	String palestrante;
	String descricao;
	int ch;
	String horaInicio;
	
	public palestra (String titulo, String descricao, String palestrante, int ch, String horaInicio) {
		this.titulo = titulo;
		this.descricao = descricao;
		this.palestrante = palestrante;
		this.ch = ch;
		this.horaInicio = horaInicio;
	}

}
